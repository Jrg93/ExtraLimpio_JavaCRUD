    private void btxEliminar_ClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btxEliminar_ClienteActionPerformed

        if (Tabla_c.getSelectedRow() >= 0) {
            int des = JOptionPane.showConfirmDialog(null, "Eliminar Cliente", "Confirmar", JOptionPane.YES_NO_OPTION);

            if (des == JOptionPane.YES_OPTION) {

                c.eliminar_c(Tabla_c);
                JOptionPane.showMessageDialog(null, "Cliente eliminado con exito");
                c.mostrar_c(Tabla_c);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debes seleccionar un campo");
        }


    }//GEN-LAST:event_btxEliminar_ClienteActionPerformed

