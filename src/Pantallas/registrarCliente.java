    private void jcbxRegionItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcbxRegionItemStateChanged

        if (evt.getStateChange() == ItemEvent.SELECTED) {

            Region reg = (Region) jcbxRegion.getSelectedItem();

            DefaultComboBoxModel cd = new DefaultComboBoxModel(c.mostrarCiudad(reg.getId_region()));
            jcbxCiudad.setModel(cd);
            jcbxComuna.removeAllItems();
        }


    }//GEN-LAST:event_jcbxRegionItemStateChanged

    private void jcbxCiudadItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcbxCiudadItemStateChanged

        if (evt.getStateChange() == ItemEvent.SELECTED) {

            Ciudad ciud = (Ciudad) jcbxCiudad.getSelectedItem();

            DefaultComboBoxModel comuna = new DefaultComboBoxModel(c.mostrarComuna(ciud.getId_ciudad()));
            jcbxComuna.setModel(comuna);
        }

    }//GEN-LAST:event_jcbxCiudadItemStateChanged

