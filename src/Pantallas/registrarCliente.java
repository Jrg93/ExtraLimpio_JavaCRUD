    private void jbtnFiltroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnFiltroActionPerformed

        String cam = jtfCampo.getText();
        if (jcbxTipoFiltro.getSelectedItem().equals("Rut")) {
            c.filtrarCliente(Tabla_c, f.formatoRutfiltro(cam), jcbxTipoFiltro);
        } else {
            c.filtrarCliente(Tabla_c, cam, jcbxTipoFiltro);
        }


    }//GEN-LAST:event_jbtnFiltroActionPerformed

