    /**
     * Al dar al boton modificar primero se verificara que algun campo de la
     * tabla vendedor este seleccionado, si no lo esta se mostrara un mensaje
     * pidiendo seleccionar un campo, de lo contrario se mostrara un mensaje
     * pidiendo la confirmacion de la modificacion.
     *
     * @param evt
     */
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (ID_v.getText().length() > 0) {
            int des = JOptionPane.showConfirmDialog(null, "Modificar vendedor", "Confirmar", JOptionPane.YES_NO_OPTION);

            if (des == JOptionPane.YES_OPTION) {

                if (v.valUpdateV(nom, app, apm, dir) && v.valNumero(num)) {
                    c.modificar_v(ID_v, f.formatoNombre(nom), f.formatoNombre(app), f.formatoNombre(apm), f.formatoNombre(dir), num);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debes seleccionar un campo");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

